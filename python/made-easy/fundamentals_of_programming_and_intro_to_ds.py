# #### Основы компьютерного программирования
#
# Что такое программирование?
# программирование - это то, как мы общаемся с компьютером. Программу определяют инструкции, которые являются конкретными и последовательными.
# И точно так же, как рецепт, когда программа составлена правильно, нас порадует конечный результат.
# Чтобы заставить компьютер сделать что-либо, инструкции должны быть записаны в виде компьютерной программы. В компьютерной программе вы должны последовательно указать, что именно вы от него хотите. Затем компьютер запускает программу и выполняет ее шаг за шагом, чтобы сделать требуемую задачу.
#
#
# #### Компьютерный алгоритм
#
# алгоритм - это конечная последовательность четко определенных, реализуемых компьютером инструкций для решения какой-то проблемы или для выполнения вычислений. Алгоритмы всегда однозначны и используются для вычислений, обработки данных, автоматизированных решений и других задач.
#
# Алгоритмы можно изображать в графической форме с использованием определенных обозначений. Полученное изображение называется блок-схемой.

# ![image.png](attachment:image.png)

# #### Что такое язык программирования?
#
# Если мы хотим дать компьютеру какие-то указания, нужен язык программирования. Разных языков программирования существует очень много. Как и у разговорных языков, у всех языков программирования есть свои правила (синтаксис) и значения (семантика).
#
# Исходный код - это то, что пишут программисты на всех языках программирования.
#
# #### Как запустить исходный код?
#
# Существуют три основных способа перевода исходного кода в машинный код:
# ♦ компиляция;
# ♦ интерпретация;
# ♦ сочетание этих двух способов.
#
# Главное отличие:
#
# - Компилятор: переводит всю программу заранее → потом запускается быстро.
#
# - Интерпретатор: переводит «на ходу» → запуск проще, но работает медленнее.
#
