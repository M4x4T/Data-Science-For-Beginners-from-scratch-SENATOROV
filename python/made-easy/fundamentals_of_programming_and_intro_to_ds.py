# #### Что такое Data Science?
#
# Data Science - это научная дисциплина, которая включает в себя извлечение ин формации из огромных объемов данных с использованием различных научных методов, алгоритмов и процессов.

# ![image.png](attachment:image.png)

# #### Зачем нам Data Science?
#
# Data Science как наука, занимающаяся изучением данных, помогает лучше прини мать решения, обнаруживать закономерности, составлять прогнозы, выполнять анализ  и создавать визуальное представление из необработанных данных. Эта наука помогает нам:
#
# - задавать правильные вопросы;
# - находить основную причину проблемы;
# - находить закономерности среди, на первый взгляд, хаотичных необработанных
# данных;
# - создавать модели для предиктивного анализа;
# - визуализировать и отображать результаты с помощью графиков, информационных панелей и т. д.;
# - наделять машины интеллектом;
# - определять лояльность клиентов с помощью анализа настроений;
# - принимать более качественные и быстрые решения;
# - рекомендовать правильный продукт нужному клиенту для развития нашего бизнеса.
#
# #### Чем занимается специалист по Data Science?
#
# специалист по анализу данных решает бизнес задачи с помощью следующих шагов:
#
# ♦ задает правильные вопросы, чтобы понять проблему;
# ♦ собирает данные из нескольких источников - корпоративные данные, общедоступные данные и т. д.;
# ♦ обрабатывает сырые данные и преобразует их в формат, подходящий для анализа;
# ♦ загружает данные в аналитическую систему - алгоритм машинного обучения или статистическая модель;
# ♦ подготавливает результаты и идеи, которые можно изложить заинтересованным сторонам.
# Всё это позволяет решить немало проблем, некоторые из них приведены далее:
# ♦ обнаружение мошенничества и выявление аномалий, например изменений схемы снятия или расходования средств с кредитной карты клиента;
# ♦ целевой и персонализированный маркетинг - персональные рассылки по электронной почте, системы рекомендаций на сайтах магазинов;
# ♦ метрические прогнозы - показатели эффективности, качества рекламных кампаний и других мероприятий;
# ♦ оценка принятия решений - обработка больших объемов данных и помощь в принятии решения, например о выдаче кредита на основе кредитных оценок;
# ♦ прогнозирующее моделирование - прогнозирование столкновения метеорита с землей на основе астрономических данных.
#
#
# ####  Профессии в области Data Science:
#
# - специалист по данным;
# - дата-инженер;
# - аналитик данных;
# - статистик;
# - архитектор данных;
# - администратор данных;
# - бизнес-аналитик;
# - менеджер данных/аналитики.
